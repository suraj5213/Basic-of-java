
public class Arithmetic_operations {

	public static void main(String[] args)
	{
		int a,b,p,q,r,s;
		a=10;
		b=5;
		
		p=a+b;
		q=a-b;
		r=a*b;
		s=a/b;
		
		System.out.println("P="+p);
		System.out.println("Q="+q);
		System.out.println("R="+r);
		System.out.println("S="+s);
	}

}

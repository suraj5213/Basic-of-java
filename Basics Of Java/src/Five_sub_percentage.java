
public class Five_sub_percentage {

	public static void main(String[] args)
	{
		int m1,m2,m3,m4,m5;
		double t,a,p;
		m1=50;
		m2=55;
		m3=60;
		m4=62;
		m5=40;
		
		t=m1+m2+m3+m4+m5;
		a=t/5;
		p=t/500*100;
		
		System.out.println("Total "+t);
		System.out.println("Average "+a);
		System.out.println("Percentage "+p);
	}

}
